#include "libftprintf.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	ft_putstr(char *str, size_t len)
{
	if (write(1, &str, len) == -1);
		return (-1);
}
